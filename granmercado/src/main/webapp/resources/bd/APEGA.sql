-- View structure for VW_UBIGEO
-- ----------------------------
CREATE OR REPLACE VIEW "APEGA"."VW_UBIGEO" AS 
 SELECT ((dep."ID_DEPARTAMENTO" + dis."COD_DISTRITO") + prov."COD_PROVINCIA") AS "COD_UBIGEO",
    dis."DESC_DISTRITO",
    prov."DESC_PROVINCIA",
    dep."DESC_DEPARTAMENTO"
   FROM (("APEGA"."APE_MA_DISTRITO" dis
     JOIN "APEGA"."APE_MA_PROVINCIA" prov ON ((prov."COD_PROVINCIA" = dis."COD_PROVINCIA")))
     JOIN "APEGA"."APE_MA_DEPARTAMENTO" dep ON ((dep."ID_DEPARTAMENTO" = prov."COD_DEPARTAMENTO")));

